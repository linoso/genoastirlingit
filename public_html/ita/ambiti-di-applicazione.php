Il nostro mercato è quindi rappresentato da tutte quelle realtà, piccole o medie, che hanno bisogno di energia; energia che tali realtà, in tutto o in parte, producono in proprio e per le quali la stessa rappresenta un costo e una risorsa critica.<br /><br />

Esistono situazioni in cui nessuna delle alternative tecnologiche attualmente disponibili sul mercato è applicabile vantaggiosamente all’attività svolta, inoltre, anche rispetto ad altre forme di produzione di <strong>energia rinnovabile</strong>, i vantaggi di una gamma di <a href="motori-disponibili.php" title="motori stirling"><strong>motori Stirling</strong></a> sono evidenti: dall’impatto ambientale e paesaggistico in sostanza nullo, alla mancanza di necessità di lunghe trafile burocratiche per l’autorizzazione dell’impianto, al sicuro risparmio economico per chi intende effettuare l’investimento a parità di energia prodotta.<br /><br />

