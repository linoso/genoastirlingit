                         <h1 class="titoletto">La nostra esperienza</h1>

                         <p>Il nostro interessamento a questo tipo di motore nasce nel 2002 quando abbiamo iniziato presso l&rsquo;officina del DICheP, Dipartimento di Ingegneria Chimica e di Processo, Facolt&agrave; di Ingegneria, Genova la progettazione e la costruzione di un primo prototipo di motore a combustione esterna di tipo Stirling. <br />
<br />La ricerca condotta, fin dagli esordi, era finalizzata al tentativo  - grazie alle nuove conoscenze tecnologiche e a sostanziali innovazioni nel metodo di costruzione - di realizzare un motore Stirling di dimensioni e costi ridotti in grado di esprimere una potenza sufficiente a renderlo economicamente appetibile per il mercato domestico e delle piccole attivit&agrave; produttive.<br />
<br />Tra il 2002 e il 2008 la ricerca si &egrave; sviluppata con  la realizzazione di diversi prototipi di Stirling perfezionati di volta in volta sino alla realizzazione di un ultimo prototipo in grado di generare una potenza di 800 Watt.<br />
<br /><strong>Nel 2009 abbiamo fondato Genoastirling e sulla base dei risultati ottenuti abbiamo sviluppato un prototipo di motore Stirling di potenza nominale di 1 Kwe in otto esemplari.</strong><br />
<br />Sette di questi esemplari sono andati a centri di ricerca e Universit&agrave; in Malesia, Cina, Bolivia, Svezia, Canada e Francia<br />
<br />L&rsquo;ottavo esemplare completo di caldaia &egrave; stato installato presso il CRIBE (Centro di Ricerca Interuniversitario sulle Biomasse da Energia) di Pisa dove nel corso del 2011 e sino a met&agrave; 2012 sono stati effettuati una serie di test sia di potenza che di affidabilit&agrave;<br />
<br />In relazione alle quattro principali innovazioni tecnologiche apportate alla struttura classica del motore Stirling abbiamo ottenuto relativo brevetto<br />
<br /><strong>I risultati dei test hanno permesso la definizione di alcuni parametri in relazione al prototipo da 3 Kwe che &egrave; stato successivamente realizzato nel corso del 2012 in 5 esemplari</strong> venduti a centri di ricerca, universit&agrave; e clienti Italiani ed Europei.<br />
<br />Con riferimento a due di questi esemplari sono stati eseguiti e sono tutt&rsquo;ora in corso sotto la nostra supervisione test di potenza e affidabilit&agrave;.<br />
<br />I motori Genoastirling frutto della nostra ricerca presentano le seguenti caratteristiche:<br />
<br /><strong>Semplicit&agrave; di costruzione</strong>: il motore pu&ograve; essere completamente disassemblato e riassemblato con utensili di uso comune in poche ore da una persona con una qualche conoscenza meccanica di base. <br />
<br /><strong>Versatilit&agrave;</strong>: i motori possono essere utilizzati sia con bruciatori a gas che su caldaie a biomassa (pellet, legna cippato, ecc.). <br />
  I Test sono stati condotti sia con bruciatori a gas che su caldaie a pellets, a legna e a fiamma inversa senza bisogno di apportare modifiche allo scambiatore caldo del motore.<br />
<br /><strong>Assenza di lubrificazione e limitato uso di guarnizioni dinamiche soggette ad usura</strong>: Al momento &egrave; presente una sola guarnizione a tenuta rotante all&rsquo;uscita dell&rsquo;albero che potrebbe essere eliminata includendo il generatore elettrico all&rsquo;interno del carter<br />
<br /><strong>Utilizzo di aria come gas di lavoro</strong>, anzich&eacute; elio o idrogeno, per permetterne l&rsquo; utilizzo anche nelle localit&agrave; pi&ugrave; isolate.<br />
  Naturalmente con l&rsquo;utilizzo dell&rsquo;elio i nostri motori esprimono una potenza superiore (circa il 40% in pi&ugrave;) ma allo stato della realizzazione &egrave; difficile prevedere la totale assenza di fughe&nbsp;nel lungo periodo.<br />
<br /><strong>Resistenza ed affidabilit&agrave;</strong>: tutti i materiali sono stati scelti e testati per poter operare nelle condizioni pi&ugrave; gravose in particolare sul lato caldo normalmente soggetto ad ossidazione e corrosione oltre che a fatica (un motore che gira a 600 gpm ha 10 pulsazioni al secondo con un salto di alcune decine di bar ad una temperatura di 700 - 800 &deg;C o superiore).<br />
<br /><strong>Il motore comincia a girare a 550&deg; C</strong> e lavora normalmente ad una temperatura di 750&deg;C (temperatura tenuta durante le prove). Pu&ograve; comunque lavorare senza problemi anche a temperature pi&ugrave; elevate (fino a 1.000 &deg;C) grazie ai materiali (AISI 310) utilizzati.<br />
<br /><strong>Pu&ograve; essere abbinato ad impianti e tecnologie esistenti</strong>, o comunque disponibili sul mercato, trasformando semplici sistemi per produrre calore in impianti di cogenerazione<br />
