            <div class="contenuto">
               <h1>Motore Stirling</h1>
				Il <strong>motore Stirling</strong> (dal nome del <a href="http://it.wikipedia.org/wiki/Robert_Stirling" target="_blank" title="Robert Stearling">Rev. Dr. Stirling</a> che sviluppò il primo esemplare nel 1816) è un motore a combustione esterna. <br /> <br />

                Il suo principio di funzionamento è estremamente semplice:  una massa costante di gas è contenuta all’interno dei cilindri, la quale riscaldandosi si espande e raffreddandosi si contrae spingendo il pistone di forza alternativamente verso l’alto o verso il basso. In funzione della cilindrata, della rapidità con cui avviene lo scambio termico e della temperatura differenziale tra il massimo caldo e il massimo freddo varia la potenza prodotta (queste sono solo alcune delle variabili in gioco).<br /> <br />
                
                I <strong>motori Stirling</strong> si dividono in tre principali categorie:<br /><br />
                
                1 - ALFA: motore con due cilindri diversi a 90° uno freddo e l’altro caldo;<br />
                2 - BETA: motore con un pistone di forza e un displacer nello stesso cilindro;<br />
                3 - GAMMA: motore con un pistone di forza e un displacer in due cilindri diversi.<br /><br />
                
                Le principali caratteristiche che rendono appetibile l’utilizzo di un motore Stirling sono:<br />
              <ul class="elenco_1">
                 <li>può essere applicato a <strong>qualsiasi fonte di calore</strong>: gas (anche prodotto da biomasse) carbone, legna, calore di scarto (ad es. prodotto da impianti di raffreddamento), calore da concentrazione solare etc., anche in abbinamento tra di loro;</li>
                 <li>le emissioni sono facilmente controllabili e di gran lunga <strong>meno nocive</strong> di quelle dei motori a combustione interna;</li>
                 <li>la sua struttura non necessita di manutenzioni complicate;</li>
                 <li>le emissioni sonore sono estremamente contenute;</li>
                 <li>il calore residuo, che non viene utilizzato per la produzione di forza, può essere <strong>recuperato</strong> e <strong>reintrodotto</strong> nel ciclo di riscaldamento dell’acqua aumentando così l’efficienza complessiva.</li>
              </ul><br /><br />
                In breve: <strong>il motore Stirling è silenzioso</strong>, user friendly e in pratica non necessità di manutenzione; può essere alimentato da un’ampia <strong>varietà di fonti energetiche</strong> e produce emissioni facilmente controllabili se non addirittura nulle. <br /><br />
                
                Tutte queste caratteristiche lo rendono utilizzabile ovunque e da chiunque. Ciò nonostante, a quasi 200 anni dalla sua invenzione,  tale tecnologia ha trovato spazi di applicazione solo in ambiti molto specifici: è stata impiegata ad esempio dall’<strong>agenzia spaziale NASA</strong> in occasione delle più recenti missioni spaziali statunitensi; inoltre la marina norvegese ha installato <strong>motori Stirling</strong> su tre sottomarini per via della loro particolare silenziosità, infine alcune ditte americane hanno applicato prototipi di motore Stirling ad impianti di produzione di energia a concentrazione solare. 
                Naturalmente tali prototipi non sono sfruttabili nel mercato degli utenti comuni, a causa dell’elevato costo di realizzazione. </div>
            
