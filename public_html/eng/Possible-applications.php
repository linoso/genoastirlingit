              <h1 class="titoletto">Possible applications</h1>
				Anyone who needs to produce heat has above all the need to produce it efficiently (economically). To be able to produce electrical energy at the same cost and without the need to increase the power of the furnace with which heat energy is produced, without a doubt increases the efficiency of the plant.<br /><br />

Our market is therefore represented by all of those small or medium concerns that need energy: energy that these firms, either totally or in part, produce on their own and which in and of itself represents a significant cost and a critical resource.<br /><br />

There are situations in which none of the technological alternatives currently available on the market may be advantageously applied to the work being carried out.  Furthermore, even compared to other forms of renewable energy production, the advantages of a range of <a href="engine-available.php" title="Stirling Engines"><strong>Stirling Engines</strong></a> are evident: from the environmental and landscape impact, being basically null, to the lack of any long bureaucratic authorisation procedures for the facility, going on to the certain economic return for anyone who intends on making the initial investment, given the quantity of energy produced. <br /><br />

Today, alongside the large power stations and the traditional distribution lines, ever more small, localised systems are working.  These, combining the production of heat energy with that of electrical power, represent in many situations an efficient solution both from the economic point of view as well as that of energy availability.<br /><br />

The Micro-Cogeneration market is being extended and established, favoured also by the new regulations surrounding it regarding the liberalisation of electrical energy production and in favour of micro-generation plants, making this type of technology ever more desirable compared to the traditional furnaces able to produce only thermal energy.<br /><br />

There are very many settings in which micro-cogeneration finds optimal conditions for application: practically all of those situations in which there is a need to produce heat energy for a prolonged number of hours over the year, such as, for example: <br />

Swimming pools and sports centres, private health care facilities and hospitals, community centres, restaurants and hotels, rest homes, schools and dormitories, supermarkets, mountain community water purification plants, farm holiday centres, nature parks and reserves, ecology oases, mountain pasture retreats, mountain refuge cottages, green house farms, agro-food industries, dairies, pasta factories, tanneries, chemical-pharmaceutical industries, textile industries, wineries, distilleries, dye houses. In addition there are all of those concerns that may be found in areas that are under-developed in regard to the traditional power lines service such as, for example, isolated houses.<br /><br />

