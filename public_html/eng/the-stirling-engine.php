               <h1>The Stirling engine</h1>
				<strong>The Stirling Engine</strong> (from the name of the inventor of the first example built in 1816,  <a href="http://en.wikipedia.org/wiki/Robert_Stirling" target="_blank" title="Robert Stearling">Rev. Dr. Stirling</a>) is an external combustion engine. <br /> <br />

                Its operational principle is extremely simple:  a constant mass of gas is contained inside of the cylinders, which when heated expands and when cooled contracts, pushing the piston with alternating force or upwards or downwards.  As a function of the cylinder displacement, the speed with which the thermal exchange takes place and the temperature differential between the maximum heat and the minimum cold the power produced varies (these are only some of the variables at play).<br /> <br />
                
               <strong>Stirling Engines</strong> may be divided into three main categories:<br /><br />
                
                1 - ALFA: engine with two 90Â° opposed cylinders, one cold and the other hot;<br />
                2 - BETA: engine with one power piston and a displacer piston in the same cylinder;<br />
                3 - GAMMA: engine with a power piston and a displacer piston in two different cylinders.<br /><br />
                
                The main features that make the use of <strong>Stirling Engine</strong> advantageous are:<br />
              <ul class="elenco_1">
                 <li>Any heat source may be applied to it: gas (even if produced by biomass) coal, wood, waste heat (i.e., produced by cooling plants), concentrated solar heat, etc., and also any of these combined; </li>
                 <li>Emissions are easily controllable and far less toxic than those from an internal combustion engines;</li>
                 <li>The engine structure does not require complicated maintenance procedures;</li>
                 <li>Noise emissions are extremely contained;</li>
                 <li>Residual heat, which is not used for the production of power, may be recovered and reintroduced in the water heating cycle, in this manner, increasing the total efficiency of the system.</li>
              </ul><br /><br />
                In short, the <strong>Stirling Engine</strong> is quiet, user friendly and practically needs no maintenance.  It may be powered by a broad variety of energy sources whilst it produces easily controllable emissions if any at all.  <br /><br />
                
                All of these features make it usable in any location and by anyone.  Nevertheless, at nearly 200 years from its invention, this technology has found application only in very specific circumstances: it has been used for example by the NASA space agency on occasion of the most recent United States space missions; the Norwegian Navy has installed Stirling Engines on three submarines due to their particularly quiet operation; and finally some American companies have applied <strong>Stirling Engine</strong> prototypes to solar concentration energy production plants. 
Naturally these prototypes are not usable in the normal consumer market due to their elevated realisation costs.
