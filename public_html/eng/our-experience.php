                         <h1 class="titoletto">Our Experience</h1>

<p>Our interest in this type of engine was established in 2002 when we started in the workshop of DICheP, Department of Chemical and Process Engineering, Faculty of Engineering, in Genoa where the design and construction of a first prototype of a external combustion engine type, the Stirling engine. <br />
<br />The research carried out, from the very beginning was aimed at trying - thanks to new technological knowledge and substantial innovations in the method of construction - to build a Stirling engine in reduced size and costs which can put out enough power to make it economically attractive for the domestic market and small productive companies. <br />
<br />Between 2002 and 2008, the research has developed several prototype Stirling engines that were improved little by little to the development of a <strong>final prototype can generate a power of 1 kw electric.</strong><br />
<br /><strong>In 2009, Genoastirling was founded and on the basis of the results obtained, we have developed 8 samples a Stirling engine prototype of a rated power of 1 kW electric. </strong><br />
<br />Seven of these specimens have gone to research centers and universities in Malaysia, China, Bolivia, Sweden, Canada and France<br />
<br />A complete engine sample of the boiler was installed at the CRIBE (Interuniversity Research Center on Biomass for Energy) in Pisa, since 2011 and up to the middle of 2012 a series of tests regarding power and reliability were carried out.<br />
<br />In relation to the four main technological innovations made &#8203;&#8203;to the structure of the classical Stirling engine we have obtained a patent application. <br />
<br /><strong>The test results allowed the definition of some parameters related to the prototype of 3-Kwe. 5 prototype units were sold  in research centers, universities and to Italian and European clients in 2012.</strong> <br />
<br />With reference to two of these specimens that have been carried out and are still in progress under our supervision in the test of power and reliability. <br />
<br />The motors are the fruits of our research which have the following features: <br />
<br /><strong>Simple construction</strong>: The engine can be completely disassembled and reassembled with household tools in just a few hours by anyone with some basic mechanical knowledge. <br />
<br /><strong>Versatility</strong>: The motors can be used with both gas burners as well as biomass heaters (pellets, wood chips, etc.).<br />
  Tests were carried out with both gas burners on boilers Pellet, Wood and reverse flame without the need to make changes to the heat exchanger of the engine.<br />
<br /><strong>The lack of lubrication and limited use of dynamic gaskets seals are subject to wear.</strong> At the moment there is only one sealing gasket rotary output shaft which could be eliminated by including the electrical generator inside the crankcase<br />
<br /><strong>Using air as the working gas</strong>, Instead of helium or hydrogen, to allow its' usage even in the most isolated places. <br />
  Of course with the use of helium, our engines express a higher power (around 40% more) but the state of the realization is difficult to predict the total absence of joints in the long run. <br />
<br /><strong>Strength and reliability</strong>: All the materials that were chosen and tested in order to operate under the toughest conditions, in particular on the hot side normally subject to oxidation and corrosion in addition to fatigue (an engine that runs at 600 gpm which has 10 pulses per second with a jump of some tens of bar at a temperature of 700-800 &deg; C or higher). <br />
<br /><strong>The motor starts to rotate at 550 &deg; C</strong> That normally works at a temperature of 750 &deg; C (temperature kept during the tests). Can still work without problems even at higher temperatures (up to 1000 &deg; C) due to the materials (AISI 310) used<br />
<br /><strong>The engine can be combined with existing structures and technologies</strong>, that are otherwise available on the market, transforming simple systems into producing heat in cogeneration structures. </p>
