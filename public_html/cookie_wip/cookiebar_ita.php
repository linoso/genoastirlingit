						<p>Un cookie è un file di testo che viene memorizzato sul computer o sul dispositivo mobile (smartphone o tablet)  dell’utente dal server di un sito web a cui l’utente accede e che può essere letto o recuperato dal server che lo ha installato in occasione di successive visite al sito medesimo. Il cookie contiene alcune informazioni (es. il server da cui proviene, un identificatore numerico, la scadenza del cookie ecc.) e permette al sito web che lo ha installato di ricordare, ad esempio, le preferenze espresse dall’utente durante la navigazione o di effettuare un acquisto, eseguire autenticazioni per accedere ad aree riservate ovvero per il monitoraggio di sessioni. Per tali cookie <strong>c.d. tecnici</strong> , non è richiesto il preventivo consenso dell’utente, salvo l’obbligo per il titolare del sito di fornire una idonea informativa. Altri cookie possono, invece, essere utilizzati per monitorare gli utenti durante la navigazione all’interno del sito anche allo scopo di inviare pubblicità e/od offrire servizi in linea con le preferenze manifestate dall’utente durante la navigazione in rete. Tali cookie, <strong> c.d. di profilazione</strong> , richiedono il consenso preventivo dell’utente e una idonea informativa sull’uso degli stessi.</p>
